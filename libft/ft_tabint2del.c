#include "libft.h"

void	ft_tabint2del(int ***tab, size_t len)
{
	size_t	i;

	i = -1;
	if (tab)
	{
		while (++i < len)
			ft_tabintdel(&((*tab)[i]));
		free(*tab);
		*tab = NULL;
	}
}
