#include "libmath.h"

double	dist_dot_3d(t_dot_3d d1, t_dot_3d d2)
{
	return (ft__sqrt((d2.x - d1.x) * (d2.x - d1.x) +\
			(d2.y - d1.y) * (d2.y - d1.y) +\
			(d2.z - d1.z) * (d2.z - d1.z), 2));
}
