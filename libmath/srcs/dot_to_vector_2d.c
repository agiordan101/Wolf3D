#include "libmath.h"

t_vector_2d	dot_to_vector_2d(t_dot_2d dot)
{
	return ((t_vector_2d){.origin = (t_dot_2d){.x = 0, .y = 0},\
							.x = dot.x, .y = dot.y});
}
